fn calculate_advanced_maths_expression<'a>(exp: &'a str) -> u128 {
    let mut current = exp.to_string();

    while let Some(cap) = ADDITION_REGEX.captures(&current) {
        let num_1 = cap.get(1).unwrap();
        let num_2 = cap.get(2).unwrap();

        let sum = num_1.as_str().parse::<u128>().unwrap() + num_2.as_str().parse::<u128>().unwrap();
        current = current.replace(&current[num_1.start()..num_2.end()], &sum.to_string());
        
    };
    let mut value = 1;

    for thing in current.split(" * ") {
        value *= thing.parse::<u128>().unwrap();
    };
    
    value
}

//cannot deal with brackets
fn calculate_expression<'a>(exp: &'a str) -> u128 {
    let mut result = 0;
    let mut operation_is_addition = true;

    for thing in exp.split(' ') {
        match thing {
            "+" => operation_is_addition = true,
            "*" => operation_is_addition = false,
            num_str => {
                let num = num_str.parse::<u128>().unwrap();
                if operation_is_addition {
                    result += num;
                } else {
                    result *= num;

                }
            }
        }
    };

    result
}

